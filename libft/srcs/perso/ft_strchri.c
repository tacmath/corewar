#include "libft.h"

int		ft_strchri(const char *s, int c)
{
	int i;

	i = -1;
	while (s[++i] != '\0')
		if ((int)s[i] == c)
			return (i);
	if (c == (int)'\0')
		return (i);
	return (-1);
}
